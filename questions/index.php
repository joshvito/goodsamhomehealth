                <h3 class="med-blu">2.  What services does the Agency provide?  Are the Agency's Aides trained to care for Alzheimer's? Parkinson's? Dementia? </h3>
                <div>
                    <p>Good Samaritan provides a full range of medical services and non-medical services.  We provide assistance with Activities of Daily Living including Bathing, Grooming, Toileting and Transferring, as well as Meal Preparation, Light Housekeeping, Laundry/Linen services and Transportation.</p>
                </div>
                <h3 class="med-blu">3.  Who does the Aides screening and what kind if screening is done?</h3>
                <div>
                    <p>The Director of Nursing is responsible for the entire hiring process.  Our screening process begins with the application.  We require every applicant to call our Employment Hotline and listen our recorded message where we explain the type of caregiver we are seeking to hire - one that emulate the biblical Good Samaritan - be Caring and Compassionate.  We do an extensive background checks, first researching the applicant through the Health Commerce System for past criminal activity, then the Nurse Registry to ensure their certifications are current and then obtain their driving record through the Department of Motor Vehicles.  We also check with past employers about their work history and make inquires with their personal references.  </p>
                </div>
                <h3 class="med-blu">4.  Are the Aides trained?  What are they trained to do?</h3>
                <div>
                    <p>Our Home Health Aides and Personal Care Aides are certified by completing a Department of Health approved curriculum.  The HHAs and PCAs are trained to provide Personal Care and assist with Activities of Daily Living such as Bathing, Toileting and Transferring.
                    <br><br>
                    Our Companion Aides complete an orientation provided by the Director of Nursing. Companion Aides provide services such as Meal Preparation, Light Housekeeping, Laundry/Linen, Transportation.</p>
                </div>
                <h3 class="med-blu">5.  Who supervises the Aides?  Are they Aides Employees or Independent Contractors?  Is the Agency withholding payroll taxes?</h3>
                <div>
                    <p>All our Aides are employees of Good Samaritan and are supervised by the Director of Nursing.  As employees their payroll taxes are withheld and paid by our payroll services company - ADP.</p>
                </div>
                <h3 class="med-blu">6.  What happens if an Aide can't come or if there is a conflict?</h3>
                <div>
                    <p>All our Aides are required to drive their own vehicle, they do not depend on public transportation or family/friends to get to their assigned patients.  Therefore, their dependability is very high.  However, if they can not attend to a patient, we have an answering service who takes call offs whenever the office is closed who relates urgent messages to us.  We will find a replacement as quickly as possible and notify the patient of the circumstance.
                    <br><br>
                    We use a telephony system that allows us to know that the Aide has arrived as schedule.  Should the Aide not call the toll-free number from the patients home with 10 minutes of their scheduled arrival, we get a text message and email.  We immediately call the patient and make them aware of the situation.</p>
                </div>
                <h3 class="med-blu">7.  What happens if the Client can't receive the Aide?  Is there flexibility in scheduling?</h3>
                <div>
                    <p>We ask that we be given 48 hours notice for schedule changes.  However, if family or friends arrive unannounced we try to remain flexible.  We understand that events come up on short notice and do our best to be flexible.  </p>
                </div>
                <h3 class="med-blu">8.  What is the minimum number of hours of service?</h3>
                <div>
                    <p>Our Aides are trying to build a relationship with each of our Patients and Clients.  In order to build that relationship, we have found that a 4 hour minimum is necessary.  In cases where we are attending to a patient on multiple days each week, we can adjust the hours per to three.</p>
                </div>
                <h3 class="med-blu">9.  What are the costs?</h3>
                <div>
                    <p>The costs vary depending on the type of Aide assigned - HHA, PCA or Companion Aide, and the number of hours.
                    <br>
                    The hourly fee range from $18.95-$22.50.</p>
